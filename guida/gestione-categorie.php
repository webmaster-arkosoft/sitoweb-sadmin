					<div class="boxguida">
						La gestione delle categorie di prodotto &egrave; una delle pi&ugrave; semplici, si esegue tramite una sola finestra.<br /><br />
						<center><img src="img/gestione_categorie.jpg"></center><br /><br />
						1. <b>"Inserimento di una nuova categoria"</b>: in questa casella potete aggiungere una nuova categoria di prodotto.<br />
						2. <b>"Modifica di una categoria"</b>: una volta selezionata la categoria da modificare, dall'apposito menu a discesa, si scrive il nuovo nome della categoria e si pu&ograve; premere su <b>"Modifica"</b>.<br />
						3. <b>"Eliminazione di una categoria"</b>: da qui si eliminano le categorie; basta selezionare come prima, la categoria da eliminare dal men&ugrave; a discesa, quindi premere su <b>"Elimina"</b>.<br /><br />
