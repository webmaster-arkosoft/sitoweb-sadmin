						Anche per eliminare una categoria, si usa la finestra di <b>"Gestione Categorie"</b>: <br /><br />
						1. selezionare la categoria da eliminare, dall'apposito menu sotto <b>"Elimina una categoria"</b>;<br />
						2. premere il pulsante <b>"Elimina"</b>.<br /><br />
