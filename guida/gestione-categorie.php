						<h2 id="3.1">3.1 Aggiungere una nuova Categoria</h2><hr>
						La procedura &egrave; estremamente semplice: baster&agrave; inserire il nome della Categoria che si vuol creare nella casella sotto <b>"Inserisci una nuova categoria"</b> , quindi premere il pulsante <b>"Inserisci"</b>. <br /><br />
						<center><img src="img/nuova_categoria.jpg"></center><br /><br />
						<div class="torna"><a href="#indice">[Torna su]</a></div>
						<p>&nbsp;</p><p>&nbsp;</p>
						
						
						<h2 id="3.2">3.2 Modificare una Categoria</h2><hr>
						Per modificare una categoria gi&agrave; esistente, si utilizza sempre la finestra di <b>"Gestione Categorie"</b>: <br /><br />
						1. selezionare la categoria da modificare, dall'apposito menu sotto <b>"Modifica una categoria"</b>;<br />
						2. inserire il nuovo nome della categoria;<br />
						3. premere il pulsante <b>"Modifica"</b>.<br /><br />
