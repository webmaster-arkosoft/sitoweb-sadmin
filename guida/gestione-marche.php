					<h2 id="desc4">4. Gestione delle Marche</h2><hr>
					<div class="boxguida">
						La gestione delle marche dei prodotti avviene nello stesso modo di aggiungere i prodotti veri e propri.	<br><br>		
						<div class="center"><img src="http://www.softwarearredamento.com/guida/img/gestione_marche.jpg" alt="" width="901" height="748"></div><br><br>
						1. <b>Totale marche (x) - (Visualizza tutti)</b>: ha la stessa funzione del <b>"Totale prodotti"</b> nella finestra di <b>"Visualizza Prodotti"</b>, quindi anche in caso di risultati di una ricerca, elenca il numero di marche trovate;<br>
						2. <b>Cerca Marca</b>: anche questo ha le stesse funzioni del corrispondente nella finestra <b>"Visualizza Prodotti"</b>;<br>
						3. <b>ID</b>: questo numero identifica la Marca all'interno dell'elenco principale;<br>
						4. <b>Nome</b>: il nome della marca;<br>
						5.<b> Modifica</b>: icona per accedere alla scheda della marca, quando la si vuole modificare;<br>
						6.<b> Elimina</b>;<br>
						7. <b>Pagina (1)</b>: indica il numero di pagina dei risultati della ricerca, o dell'elenco completo.<br>
						In caso di pagine troppo numerose, vengono visualizzati due comandi (<b>"Prima"</b> e  <b>"Ultima"</b>) che portano rispettivamente alla PRIMA e all'ULTIMA delle pagine dei risultati della ricerca o dell'elenco.
						<p>&nbsp;</p><p>&nbsp;</p>
						
						<h2 id="desc4.1">4.1 Aggiungere una nuova Marca</h2><hr>
						Per questa operazione, bastano quattro semplici passaggi:<br><br>		
						<div class="center"><img src="http://www.softwarearredamento.com/guida/img/aggiungi_marca.jpg" alt="" width="900" height="762"></div><br><br>
						1. inserire il nome della Marca in <b>"Nome Marca"</b>;<br>
						2. scrivere una descrizione della Marca che si sta aggiungendo, con la possibilit&agrave; di formattare il testo, anche in parte, assegnandogli diversi colori e dimensioni, sottolineando o scrivendolo in corsivo;<br>
						3. in caso si voglia assegnare un logo alla Marca, lo si pu&ograve; fare cliccando su <b>"Scegli File"</b> sotto <b>"Nuova Immagine Marca"</b>, selezionare il file - <strong>RIGOROSAMENTE in formato jpg, gif, png</strong> - e confermare con il pulsante <b>"Apri"</b> nella finestra di selezione;<br>
						4. confermare con <b>"Aggiungi Marca"</b>.<br>
						<div class="torna"><a href="#indice">[Torna su]</a></div>
						<p>&nbsp;</p><p>&nbsp;</p>
						
						<h2 id="desc4.2">4.2 Modificare una Marca</h2><hr>
						Per modificare una Marca, occorre accedere alla finestra di <b>"Visualizza Marche"</b>, quindi:<br><br>		
						<img src="http://www.softwarearredamento.com/guida/img/modifica.jpg" alt="" width="81" height="44"><br><br>
						1. cliccare l'icona <b>"Modifica"</b> corrispondente alla riga della Marca, rappresentata dal block notes, per aprire la finestra di <b>"Modifica Marca"</b> vera e propria;<br>
						2. la finestra si presenta come quella di <b>"Aggiungi Marca"</b>, basta infatti andare nella casella che si vuole modificare ed effettuare i cambiamenti voluti.<br>
						<div class="torna"><a href="#indice">[Torna su]</a></div>
						<p>&nbsp;</p><p>&nbsp;</p>
						
						<h2 id="desc4.3">2.3 Eliminare una Marca</h2><hr>
						La procedura di eliminazione di una marca &egrave; simile a quella utilizzata per modificarla, bisogna innanzitutto aprire la finestra <b>"Visualizza Marche"</b>.			
						1. per eliminare una marca, cliccare sull'icona <b>"Elimina"</b> corrispondente alla sua riga, rappresentata da un esagono rosso con una X bianca.<br><br>
						<img src="http://www.softwarearredamento.com/guida/img/elimina.jpg" alt="" width="81" height="44"><br><br>
