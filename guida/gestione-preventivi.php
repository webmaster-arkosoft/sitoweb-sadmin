						<h2 id="7.1">7.1 Visualizzare un Preventivo</h2><hr>
						Per visualizzare un Preventivo, occorre innanzitutto accedere alla finestra <b>"Visualizza Preventivi"</b>, quindi:<br /><br />
						
						1. premere il comando <b>"Visualizza"</b> corrispondente alla riga del Preventivo.<br /><br />
						
						La finestra di <b>"Visualizza Preventivo"</b> &egrave; cos&igrave; strutturata:<br /><br />
						<center><img src="img/preventivo.jpg"></center><br /><br />
						1. <b>(Nome Utente) ha richiesto il seguente preventivo</b>: qui viene indicato il nome dell'Utente che ha effettuato la richiesta di Preventivo;<br />
						2. <b>Richiesta</b>: la richiesta effettuata dall'Utente;<br />
						3. <b>Ulteriori Informazioni</b>: qui vengono riportare alcune informazioni utili sull'Utente, come <b>Nome</b>, <b>Paese</b> e <b>(Provincia)</b> di provenienza, <b>Telefono</b> e <b>E-mail</b>;<br /> 
						4. <b>Preventivo n. (x) del (data)</b>: il numero progressivo del Preventivo e la data in cui verr&agrave; inviato il preventivo all'utente;<br />
						5. <b>Prevista Consegna</b>: qui viene indicato il tempo impiegato per la consegna, si pu&ograve; indicare qualsiasi tipo di intervallo, in giorni, settimane o anche mesi;<br />
						6. <b>Validit&agrave; Offerta</b>: indica la durata della validit&agrave; dell'eventuale offerta;<br />
						7. <b>Condizioni dell'offerta</b>: qui si possono indicare le condizioni dell'eventuale offerta;<br />
						8. <b>(Nome del Prodotto)</b>;<br />
						9. <b>Elimina</b>: comando per eliminare il singolo prodotto dal preventivo;<br />
						10. <b>Immagine del Prodotto</b>;<br />
						11. <b>Descrizione del Prodotto</b>;<br />
						12. <b>Prezzo dell'Articolo</b>: il prezzo proposto dall'amministratore per quel mobile;<br />
						13. <b>Nota</b>: qui si pu&ograve; aggiungere una nota personale per ogni singolo prodotto, da aggiungere al Preventivo, per eventuali spiegazioni;<br />
						14. <b>Salva Preventivo</b>: memorizza il preventivo per un successivo invio;<br />
						15. <b>Aggiungi Prodotto</b>: da qui &egrave; possibile aggiungere nuovi prodotti al Preventivo, tramite una comoda finestra;<br />
						16. <b>Anteprima Preventivo</b>: visualizza un'anteprima di come sar&agrave; visualizzato dall'utente;<br />
						17. <b>Invia Preventivo</b>: invia il preventivo al rispettivo utente;<br />
						18. <b>Stampa Preventivo</b>: avvia il procedimento di stampa del preventivo.<br /><br />
						
						<b>Nota</b>: i pulsanti <b>"Anteprima preventivo"</b>, <b>"invia preventivo"</b> e <b>"Stampa preventivo"</b> diventano utilizzabili solo se il preventivo &egrave; stato salvato.
