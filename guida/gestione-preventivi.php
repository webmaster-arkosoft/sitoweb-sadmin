						<h2 id="desc7.6">7.6 Ricerca un Preventivo</h2><hr>
						Per effettuare una ricerca dei Preventivi, si accede alla finestra <b>"Visualizza Preventivi"</b>, quindi preme il pulsante <b>"Cerca un Preventivo"</b>, che apre una sezione dalla quale inserire i dati di identificazione dei preventivi, anche uno solo dei seguenti:<br><br>
						<div class="center"><img src="http://www.softwarearredamento.com/guida/img/ricerca_preventivo.jpg" alt="" width="901" height="748"></div><br><br>
						1. <b>Numero Preventivo</b>: il numero del Preventivo che si vuole cercare;<br>
						2. <b>Nome o Cognome</b>: per cercare tutti i Preventivi richiesti da una o pi&ugrave; persone che abbiano quel <b>"Nome"</b> e/o <b>"Cognome"</b>;<br>
						3. <b>Data Preventivo</b>: per cercare uno o pi&ugrave; Preventivi richiesti in una determinata data, o tutti quelli di un singolo <b>"Mese"</b>, <b>"Giorno"</b> o <b>"Anno"</b>;<br> 
						4. <b>Mobile</b>: da qui si pu&ograve; effettuare una ricerca di prodotto specifico anche in base ad una determinata <b>"Marca"</b> selezionabile dal men&ugrave; a discesa;<br>
						5. <b>Cerca</b>: per confermare i criteri della ricerca, ed eseguirla.<br><br>
						I risultati della ricerca verranno visualizzati nell'elenco sottostante, il cui funzionamento &egrave; stato spiegato nell'apposita sezione di questa guida.<br><br>
