					<h2 id="desc7">7. Gestione dei Preventivi</h2><hr>
					<div class="boxguida">
						<br><br>
						<div class="center"><img src="http://www.softwarearredamento.com/guida/img/visualizza_preventivi.jpg" alt="" width="901" height="748"></div><br><br>
						
						1. <b>Totale Utenti (x) - (Visualizza tutti)</b>: ha la stessa funzione del <b>"Totale prodotti"</b> nella finestra di <b>"Visualizza Prodotti"</b>, quindi anche in caso di risultati di una ricerca, elenca il numero di Utenti corrispondenti ai criteri della ricerca;<br>
						2. <b>Cerca Preventivo</b>: motore di ricerca per i preventivi richiesti;<br>
						3. <b>Richiesta</b>: indica che un Utente - indicato dal nome in grassetto ha richiesto un preventivo;<br>
						4. <b>Data</b>: qui viene indicata la data in cui &egrave; stata effettuata la richiesta di Preventivo, in ordine decrescente;<br>
						5. <b>Visualizza</b>: per accedere al Preventivo e visualizzarne i prodotti richiesti;<br>
						6. <b>Elimina</b>;<br>
						7. <b>Invia</b>: da qui ordina l'invio della Newsletter a tutti gli utenti per i quali si &egrave; selezionata la casella <b>"Iscrivi l'utente alla Newsletter"</b> nella loro scheda;<br>
						8. <b>Pagina (1)</b>: indica il numero di pagina dei risultati della ricerca, o dell'elenco completo. <br>
						In caso di pagine troppo numerose, vengono visualizzati due comandi (<b>"Prima"</b> e
						<b>"Ultima"</b>) che portano rispettivamente alla PRIMA e all'ULTIMA delle pagine dei risultati della ricerca o dell'elenco.<br><br>
