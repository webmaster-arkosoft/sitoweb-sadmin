						<h2 id="7.5">7.5 Stampare un Preventivo</h2><hr>
						Anche per stampare il Preventivo visualizzato, si utilizza la funzione <b>"Visualizza"</b> dalla finestra <b>"Visualizza Preventivi"</b>, quindi:<br /><br />			
						
						1. premere il pulsante <b>"Stampa Preventivo"</b>;<br />
						2. impostare i parametri di stampa, dalla normale finestra di sistema per la gestione della stampa, che verr&agrave; visualizzata.<br /><br />
						<center><img src="img/stampa_preventivo.jpg"></center><br /><br />
						<b>Nota</b>: Nella stampa del preventivo viene indicato anche il prezzo totale del preventivo.
