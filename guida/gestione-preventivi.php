						<h2 id="desc7.2">7.2 Aggiungere un prodotto ad un preventivo</h2><hr>
						Per aggiungere un Prodotto ad un Preventivo, bisogna utilizzare la funzione <b>"Visualizza"</b> dalla finestra <b>"Visualizza Preventivi"</b>, quindi:<br><br>
						
						1. cliccare sul pulsante <b>"Aggiungi Prodotto"</b>;<br>
						2. dalla finestra <b>"Lista Prodotti"</b> che si apre, cercare il prodotto da inserire ricercandolo tramite il campo del <b>"Titolo"</b>, della <b>"Categoria"</b> o della <b>"Marca"</b> del mobile, dall'apposito men&ugrave; a discesa;<br><br>
						<div class="center"><img src="http://www.softwarearredamento.com/guida/img/lista_prodotti.jpg" alt="" width="552" height="536"></div><br><br>
						3. premere su <b>"Visualizza"</b> per ottenere la lista dei prodotti trovati;<br> 
						4. selezionare il mobile da aggiungere al preventivo;<br>
						5. premere su <b>"Aggiungi al Preventivo"</b> per confermare l'operazione.<br><br>
						
						<b>Nota</b>: ripetere l'operazione per ogni prodotto che si intende aggiungere al preventivo.
						<div class="torna"><a href="#indice">[Torna su]</a></div>
