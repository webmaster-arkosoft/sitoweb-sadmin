						<h2 id="desc7.4">7.4 Inviare un Preventivo</h2><hr>
						Anche per inviare il Preventivo visualizzato, si utilizza la funzione <b>"Visualizza"</b> dalla finestra <b>"Visualizza Preventivo"</b>, quindi: <br><br>			
						1. premere il pulsante <b>"Invia Preventivo"</b>.<br><br>
						<div class="center"><img src="http://www.softwarearredamento.com/guida/img/invio_preventivo.jpg" alt="" width="901" height="748"></div><br><br>
						Il preventivo verr&agrave; inviato all'utente cos&igrave; come appare in <b>"Anteprima preventivo"</b>.<br><br>
						<b>Nota</b>: prima di inviare il preventivo &egrave; consigliabile consultare sempre l'anteprima.
