					<h2 id="5">5. Gestione degli Utenti</h2><hr>
					<div class="boxguida">
						<br /><br />
						<center><img src="img/visualizza_utenti.jpg"></center><br /><br />
						
						1. <b>Totale Utenti (x) - (Visualizza tutti)</b>: ha la stessa funzione del <b>"Totale prodotti"</b> nella finestra di <b>"Visualizza Prodotti"</b>, quindi anche in caso di risultati di una ricerca, elenca il numero di Utenti corrispondenti ai criteri della ricerca;<br />
						2. <b>Cerca Utente</b>: anche questo ha le stesse funzioni del corrispondente nella finestra <b>"Visualizza Prodotti"</b>;<br />
						3. <b>Legenda</b>: qui viene data spiegazione dei diversi colori delle icone corrispondenti allo <b>"Stato"</b> dell'Utente;<br />			
						4. <b>ID</b>: questo numero identifica l'Utente all'interno dell'elenco principale;<br />
						5. <b>Nome</b>: il nome dell'utente;<br />
						6. <b>Stato</b>: l'icona indica se l'utente &egrave; attivo o disattivato, assumendo il colore corrispondente alla <b>"Legenda"</b>;<br />
						7. <b>Modifica</b>: icona per accedere alla scheda dell'Utente, quando la si vuole modificare;<br />
						8. <b>Elimina</b>;<br />
						9. <b>Pagina (1)</b>: indica il numero di pagina dei risultati della ricerca, o dell'elenco completo. <br />
						In caso di pagine troppo numerose, vengono visualizzati due comandi (<b>"Prima"</b> e
						<b>"Ultima"</b>) che portano rispettivamente alla PRIMA e all'ULTIMA delle pagine dei risultati della ricerca o dell'elenco.<br /><br />
						<div class="torna"><a href="#indice">[Torna su]</a></div>
						<p>&nbsp;</p><p>&nbsp;</p>
						
						<h2 id="5.1">5.1 Aggiungere un nuovo Utente</h2><hr>
						L'aggiunta di un nuovo Utente &egrave; una delle procedure pi&ugrave; semplici di S-Admin: baster&agrave; riempire tutti i campi contrassegnati dall'asterisco con i dati anagrafici e i contatti dell'Utente che si sta andando a registrare.<br /><br />
						<center><img src="img/aggiungi_utente.jpg"></center><br /><br />
						Alcune spiegazioni sono comunque dovute, per maggiore chiarezza:<br /><br />
						1. il campo <b>"CAP"</b> (Codice di Avviamento Postale) accetta solo numeri;<br />
						2. <b>"Newsletter"</b> permette di stabilire se l'utente che si sta inserendo, deve essere iscritto alla newsletter del sito, semplicemente spuntando la casella accanto a <b>"Iscrivi l'utente alle Newsletter"</b>;<br />
						3. si pu&ograve; decidere il <b>"Ruolo"</b> che l'utente avr&agrave; all'interno del sito, ovvero se <b>"Utente Standard"</b> o <b>"Amministratore"</b>, scegliendolo dal men&ugrave; a discesa; l'utente di tipo <b>"Amministratore"</b> pu&ograve; accedere a <b>S-ADMIN</b> e utilizzare le funzioni di gestione del sito;<br /> 
						Quando avrete inserito tutti i dati degli utenti, cliccate su <b>"Inserisci"</b> per confermare il tutto.<br />
						<div class="torna"><a href="#indice">[Torna su]</a></div>
						<p>&nbsp;</p><p>&nbsp;</p>
						
						<h2 id="5.2">5.2 Bloccare un nuovo Utente</h2><hr>
						Per bloccare un Utente, occorre entrare nella schermata di <b>"Visualizza Utenti"</b>, quindi:<br /><br />
						1. cliccare l'icona <b>"stato"</b> corrispondente alla riga dell'Utente, rappresentata da un omino di colore verde(che indica che l'utente &egrave; sbloccato), per cambiare lo stato dell'utente da <b>sbloccato</b> a <b>bloccato</b>;<br /><br />
						<img src="img/blocca_utente.jpg"><br /><br />
						<div class="torna"><a href="#indice">[Torna su]</a></div>
						<p>&nbsp;</p><p>&nbsp;</p>
						
						<h2 id="5.3">5.3 Sbloccare un nuovo Utente</h2><hr>
						Per sbloccare un Utente, occorre entrare nella schermata di <b>"Visualizza Utenti"</b>, quindi:<br /><br />
						1. cliccare l'icona <b>"stato"</b> corrispondente alla riga dell'Utente, rappresentata da un omino di colore rosso(che indica che l'utente &egrave; bloccato), per cambiare lo stato dell'utente da <b>bloccato</b> a <b>sbloccato</b>;<br /><br />
						<img src="img/sblocca_utente.jpg"><br /><br />
						<div class="torna"><a href="#indice">[Torna su]</a></div>
						<p>&nbsp;</p><p>&nbsp;</p>
									
						<h2 id="5.4">5.4 Modificare un utente</h2><hr>
						Per modificare un Utente, occorre entrare nella schermata di <b>"Visualizza Utenti"</b>, quindi:<br /><br />
						
						1. cliccare l'icona <b>"Modifica"</b> corrispondente alla riga dell'Utente, rappresentata dal block notes, per aprire la finestra di <b>"Modifica Utente"</b> vera e propria;<br /><br />
						<img src="img/modifica.jpg"><br /><br />
						2. la finestra si presenta come quella di <b>"Aggiungi Utente"</b>, basta infatti andare nella casella che si vuole modificare ed effettuare i cambiamenti voluti.<br /><br />
						<div class="torna"><a href="#indice">[Torna su]</a></div>
