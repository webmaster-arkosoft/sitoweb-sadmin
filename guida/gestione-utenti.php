						<h2 id="desc5.1">5.1 Aggiungere un nuovo Utente</h2><hr>
						L'aggiunta di un nuovo Utente &egrave; una delle procedure pi&ugrave; semplici di S-Admin: baster&agrave; riempire tutti i campi contrassegnati dall'asterisco con i dati anagrafici e i contatti dell'Utente che si sta andando a registrare.<br><br>
						<div class="center"><img src="http://www.softwarearredamento.com/guida/img/aggiungi_utente.jpg" alt="" width="910" height="766"></div><br><br>
						Alcune spiegazioni sono comunque dovute, per maggiore chiarezza:<br><br>
						1. il campo <b>"CAP"</b> (Codice di Avviamento Postale) accetta solo numeri;<br>
						2. <b>"Newsletter"</b> permette di stabilire se l'utente che si sta inserendo, deve essere iscritto alla newsletter del sito, semplicemente spuntando la casella accanto a <b>"Iscrivi l'utente alle Newsletter"</b>;<br>
						3. si pu&ograve; decidere il <b>"Ruolo"</b> che l'utente avr&agrave; all'interno del sito, ovvero se <b>"Utente Standard"</b> o <b>"Amministratore"</b>, scegliendolo dal men&ugrave; a discesa; l'utente di tipo <b>"Amministratore"</b> pu&ograve; accedere a <b>S-ADMIN</b> e utilizzare le funzioni di gestione del sito;<br> 
						Quando avrete inserito tutti i dati degli utenti, cliccate su <b>"Inserisci"</b> per confermare il tutto.<br>
						<div class="torna"><a href="#indice">[Torna su]</a></div>
