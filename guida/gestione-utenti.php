						<h2 id="desc5.5">5.5 Eliminare un utente</h2><hr>
						Per eliminare un Utente, bisogna accedere alla finestra <b>"Visualizza Utenti"</b>:<br><br>	
						
						1. per eliminare un Utente, cliccare sull'icona <b>"Elimina"</b> corrispondente alla sua riga, rappresentata da un esagono rosso con una X bianca.<br><br>
