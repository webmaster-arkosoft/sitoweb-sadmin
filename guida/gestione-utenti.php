						<h2 id="desc5.2">5.2 Bloccare un nuovo Utente</h2><hr>
						Per bloccare un Utente, occorre entrare nella schermata di <b>"Visualizza Utenti"</b>, quindi:<br><br>
						1. cliccare l'icona <b>"stato"</b> corrispondente alla riga dell'Utente, rappresentata da un omino di colore verde(che indica che l'utente &egrave; sbloccato), per cambiare lo stato dell'utente da <b>sbloccato</b> a <b>bloccato</b>;<br><br>
						<img src="http://www.softwarearredamento.com/guida/img/blocca_utente.jpg" alt="" width="81" height="44"><br><br>
						<div class="torna"><a href="#indice">[Torna su]</a></div>
						<p>&nbsp;</p><p>&nbsp;</p>
						
						<h2 id="desc5.3">5.3 Sbloccare un nuovo Utente</h2><hr>
						Per sbloccare un Utente, occorre entrare nella schermata di <b>"Visualizza Utenti"</b>, quindi:<br><br>
						1. cliccare l'icona <b>"stato"</b> corrispondente alla riga dell'Utente, rappresentata da un omino di colore rosso(che indica che l'utente &egrave; bloccato), per cambiare lo stato dell'utente da <b>bloccato</b> a <b>sbloccato</b>;<br><br>
						<img src="http://www.softwarearredamento.com/guida/img/sblocca_utente.jpg" alt="" width="81" height="44"><br><br>
						<div class="torna"><a href="#indice">[Torna su]</a></div>
						<p>&nbsp;</p><p>&nbsp;</p>
									
						<h2 id="desc5.4">5.4 Modificare un utente</h2><hr>
						Per modificare un Utente, occorre entrare nella schermata di <b>"Visualizza Utenti"</b>, quindi:<br><br>
						
						1. cliccare l'icona <b>"Modifica"</b> corrispondente alla riga dell'Utente, rappresentata dal block notes, per aprire la finestra di <b>"Modifica Utente"</b> vera e propria;<br><br>
						<img src="http://www.softwarearredamento.com/guida/img/modifica.jpg" width="81" height="44"><br><br>
						2. la finestra si presenta come quella di <b>"Aggiungi Utente"</b>, basta infatti andare nella casella che si vuole modificare ed effettuare i cambiamenti voluti.<br><br>
						<div class="torna"><a href="#indice">[Torna su]</a></div>
