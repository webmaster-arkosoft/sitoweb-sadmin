					<h2 id="desc5">5. Gestione degli Utenti</h2><hr>
					<div class="boxguida">
						<br><br>
						<div class="center"><img src="http://www.softwarearredamento.com/guida/img/visualizza_utenti.jpg" alt="" width="900" height="676"></div><br><br>
						
						1. <b>Totale Utenti (x) - (Visualizza tutti)</b>: ha la stessa funzione del <b>"Totale prodotti"</b> nella finestra di <b>"Visualizza Prodotti"</b>, quindi anche in caso di risultati di una ricerca, elenca il numero di Utenti corrispondenti ai criteri della ricerca;<br>
						2. <b>Cerca Utente</b>: anche questo ha le stesse funzioni del corrispondente nella finestra <b>"Visualizza Prodotti"</b>;<br>
						3. <b>Legenda</b>: qui viene data spiegazione dei diversi colori delle icone corrispondenti allo <b>"Stato"</b> dell'Utente;<br>			
						4. <b>ID</b>: questo numero identifica l'Utente all'interno dell'elenco principale;<br>
						5. <b>Nome</b>: il nome dell'utente;<br>
						6. <b>Stato</b>: l'icona indica se l'utente &egrave; attivo o disattivato, assumendo il colore corrispondente alla <b>"Legenda"</b>;<br>
						7. <b>Modifica</b>: icona per accedere alla scheda dell'Utente, quando la si vuole modificare;<br>
						8. <b>Elimina</b>;<br>
						9. <b>Pagina (1)</b>: indica il numero di pagina dei risultati della ricerca, o dell'elenco completo. <br>
						In caso di pagine troppo numerose, vengono visualizzati due comandi (<b>"Prima"</b> e
						<b>"Ultima"</b>) che portano rispettivamente alla PRIMA e all'ULTIMA delle pagine dei risultati della ricerca o dell'elenco.<br><br>
