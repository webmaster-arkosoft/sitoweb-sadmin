					<h2 id="desc6">6. Gestione delle Newsletter</h2><hr>
					<div class="boxguida">
						<br><br>
						<div class="center"><img src="http://www.softwarearredamento.com/guida/img/visualizza_newsletter.jpg" alt="" width="901" height="748"></div><br><br>
						
						1. <b>Totale Utenti (x) - (Visualizza tutte)</b>: ha la stessa funzione del <b>"Totale prodotti"</b> nella finestra di <b>"Visualizza Prodotti"</b>, quindi anche in caso di risultati di una ricerca, elenca il numero di Utenti corrispondenti ai criteri della ricerca;<br>
						2. <b>Cerca Newsletter</b>: anche questo ha le stesse funzioni del corrispondente nella finestra <b>"Visualizza Prodotti"</b>;<br>
						3. <b>ID</b>: questo numero identifica la Newsletter all'interno dell'elenco principale;<br>
						4. <b>Nome</b>: il nome della Newsletter;<br>
						5. <b>Modifica</b>: icona per accedere alla scheda della newsletter, quando la si vuole modificare;<br>
						6. <b>Elimina</b>;<br>
						7. <b>Invia</b>: da qui ordina l'invio della Newsletter a tutti gli utenti per i quali si &egrave; selezionata la casella <b>"Iscrivi l'utente alla Newsletter"</b> nella loro scheda;<br>
						8. <b>Pagina (1)</b>: indica il numero di pagina dei risultati della ricerca, o dell'elenco completo. <br>
						In caso di pagine troppo numerose, vengono visualizzati due comandi (<b>"Prima"</b> e
						<b>"Ultima"</b>) che portano rispettivamente alla PRIMA e all'ULTIMA delle pagine dei risultati della ricerca o dell'elenco.<br><br>
						<div class="torna"><a href="#indice">[Torna su]</a></div>
						<p>&nbsp;</p><p>&nbsp;</p>
						
						<h2 id="desc6.1">6.1 Creazione di una Newsletter</h2><hr>
						La creazione delle Newletter prevede pochi e semplici passaggi:<br><br>
						<div class="center"><img src="http://www.softwarearredamento.com/guida/img/aggiungi_newsletter.jpg" alt="" width="901" height="748"></div><br><br>
						1. inserire l'<b>"Oggetto"</b> della Newsletter, ovvero l'argomento della stessa;<br>
						2. inserire il corpo dell'oggetto, con gli strumenti per modificare la formattazione del testo, inserendo il grassetto, il corsivo, cambiando il colore del testo, e molto altro;<br>
						3. cliccare su <b>"Salva Newsletter"</b> per confermare.<br> 
						<div class="torna"><a href="#indice">[Torna su]</a></div>
						<p>&nbsp;</p><p>&nbsp;</p>
									
						<h2 id="desc6.2">6.2 Modificare una newsletter</h2><hr>
						Per modificare una Newsletter esistente, si accede alla finestra <b>"Visualizza Newsletter"</b>, quindi:<br><br>
						
						1. cliccare l'icona <b>"Modifica"</b> corrispondente alla sua riga, rappresentata dal block notes, per aprire la finestra di <b>"Modifica Newsletter"</b> vera e propria;<br><br>
						<img src="http://www.softwarearredamento.com/guida/img/modifica.jpg" alt="" width="81" height="44"><br><br>
						2. la finestra si presenta come quella di <b>"Aggiungi Newsletter"</b>, basta infatti andare nella casella che si vuole modificare ed effettuare i cambiamenti voluti.<br><br>
						<div class="torna"><a href="#indice">[Torna su]</a></div>
						<p>&nbsp;</p><p>&nbsp;</p>
						
						<h2 id="desc6.3">6.3 Eliminare una newsletter</h2><hr>
						Anche per eliminare una Newsletter, si accede a <b>"Visualizza Newsletter"</b>, quindi:<br><br>	
						
						1. clicca sull'icona <b>"Elimina"</b> corrispondente alla sua riga, rappresentata da un esagono rosso con una X bianca.<br><br>
						<img src="http://www.softwarearredamento.com/guida/img/elimina.jpg" alt="" width="81" height="44"><br><br>
						<div class="torna"><a href="#indice">[Torna su]</a></div>
						<p>&nbsp;</p><p>&nbsp;</p>
						
						<h2 id="desc6.4">6.4 Inviare una newsletter</h2><hr>
						Per inviare una Newsletter, si accede a <b>"Visualizza Newsletter"</b>, quindi:<br><br>	
						
						1. clicca sul <b>"Invia"</b> nella riga corrispondente alla Newsletter che si vuole inviare.<br><br>
						
						<b>NOTA</b>: la Newsletter sar&agrave; inviata a tutti quegli Utenti per i quali si &egrave; selezionata la casella <b>"Iscrivi l'Utente alla Newsletter"</b> nella loro scheda.
