					<h2 id="1">1. Accesso come amministratore</h2><hr>
					<div class="boxguida">
						Per Amministratore intendiamo l'utente principale di S-admin, e cio&egrave; colui il quale gestisce
						tutto il sito e il pannello, con la possibilit&agrave; di inserire ed eliminare prodotti e utenti, elaborare
						le newsletter aziendali con tutte le ultime novit&agrave;. <p>&nbsp;</p>
						<center><img src="img/bacheca_amministratore.jpg"></center><br /><br />
						<div class="torna"><a href="#indice">[Torna su]</a></div>
						<h2 id="1.1">1.1 Bacheca dell'Amministratore</h2><hr>
						La <b>"Bacheca"</b> &egrave; la prima pagina che viene caricata quando si accede a <b>S-ADMIN</b>. Da questa pagina potete accedere a tutte le sezioni, dalla Gestione dei Prodotti ai Preventivi.<br /><br />
						
						Nel men&ugrave; sinistro si trovano le voci inerenti le varie sezioni, mentre nella zona principale, vengono mostrati nell'ordine:<br /><br />
						
						1. <b>"Ultimi 8 Utenti Registrati":</b> in questo riquadro vengono elencati, come si pu&ograve; evincere dal titolo, gli ultimi 8 utenti registrati al sito.<br />
						2. <b>"Stato Attuale":</b> qui si trovano notizie riguardo tutto quello che &egrave; stato inserito nel sito, dai prodotti al numero di utenti registrati.<br />
						3. <b>"Mobili pi&ugrave; Visualizzati":</b> elenco dei mobili pi&ugrave; visti dai visitatori, col numero di visite ricevute.<br />
						4. <b>"Avviso":</b> permette di inviare un messaggio ad Arkosoft per segnalare un eventuale malfunzionamento di S-Admin.<br />
						5. <b>"Profilo":</b> permette di modificare i dati dell'amministratore che ha effettuato l'accesso al pannello.<br />
						6. <b>"Esci dall'account":</b> per uscire da S-Admin e tornare al sito vero e proprio.<br />
						7. &nbsp;&nbsp;<b>Pulsanti laterali:</b> Nel men&ugrave; sinistro del pannello trovate i pulsanti che vi permettono di eseguire tutte le operazioni di amministrazione del sito, dall'inserimento dei prodotti, alla creazione dei preventivi da inviare ai clienti o agli utenti iscritti al vostro sito.<br /><br />
						
						Vediamo nel dettaglio:<br /><br /><br /><br />
						
						<b>Pulsante "BACHECA"</b><br /><br />
						<img src="img/pulsante_bacheca.jpg"><br /><br />
						Questo pulsante permette di ritornare alla <b>Bacheca dell'Amministratore</b>.<br /><br /><br /><br />
						
						<b>Pulsante "BLOG"</b><br /><br />
						<img src="img/pulsante_blog.jpg"><br /><br />
						Tramite questo pulsante, potete accedere al blog del vostro sito di arredamento. Il blog viene fornito insieme a <b>S-Admin</b>.<br /><br /><br /><br />
						
						<b>Pulsante "STATISTICHE"</b><br /><br />
						<img src="img/pulsante_statistiche.jpg"><br /><br />
						Con questo pulsante, potete accedere al servizio di Google Analytics, che vi permette di
						monitorare le statistiche del sito, con la possibilit&agrave; di conoscere le parole chiave con cui i
						visitatori trovano il vostro sito di arredamento sui motori di ricerca e di svolgere un'indagine
						di mercato. Anche questo servizio &egrave; incluso con S-Admin.<br /><br /><br /><br />
						
						<b>Pulsante "PRODOTTI"</b><br /><br />
						<img src="img/pulsante_prodotti.jpg"><br /><br />
						Cliccando sulla voce "Prodotti", avete la possibilit&agrave; di inserirne uno nuovo o di modificarne uno gi&agrave; esistente.<br /><br />
						<b>"Aggiungi nuovo":</b> serve per inserire i prodotti da esporre nel vostro sito. Potete inserire una breve descrizione del prodotto, accompagnata da una o pi&ugrave; immagini la prima delle quali viene visualizzata come anteprima, scegliere la Marca del prodotto, indicare le misure del prodotto, e decidere infine se inserire o meno il prezzo, nel caso vogliate proporre un'offerta.<br /><br />
						<b>"Visualizza Prodotti":</b> consente di avere un riepilogo dei prodotti gi&agrave; inseriti nel catalogo, col numero totale degli stessi e avendo la possibilit&agrave; di visualizzarli per Marca, altrimenti &egrave; sempre possibile effettuare una ricerca di un prodotto specifico. Potete inoltre modificare i dati del prodotto o eliminarlo dal catalogo o anche bloccarlo per indicare che non &egrave; disponibile.<br /><br /><br /><br />
						
						<b>Pulsante "CATEGORIE"</b><br /><br />
						<img src="img/pulsante_categorie.jpg"><br /><br />
						con questo pulsante potete accedere alla schermata nella quale potete gestire le categorie dei prodotti, inserendone di nuove, o modificando o eliminandone una o pi&ugrave;. <br /><br /><br /><br />
						
						<b>Pulsante "MARCHE"</b><br /><br />
						<img src="img/pulsante_marche.jpg"><br /><br />
						Cliccando sulla voce "Marche", avete la possibilit&agrave; di inserirne una nuova o di modificarne una gi&agrave; esistente. Anche per la Marca, come per i prodotti, &egrave; possibile inserire una descrizione e un logo.<br /><br />
						<b>"Aggiungi nuova":</b> permette di inserire le marche dei prodotti in catalogo.<br /><br />
						<b>"Visualizza Marche":</b> permette di visualizzare, modificare o eliminare le marche di prodotti, con la possibilit&agrave; di ricercare una Marca specifica.<br /><br /><br /><br />
					
						<b>Pulsante "UTENTI"</b><br /><br />
						<img src="img/pulsante_utenti.jpg"><br /><br />
						Cliccando sulla voce "Utenti", avete la possibilit&agrave; di inserire e modificare gli utenti del vostro sito.<br /><br />
						<b>"Aggiungi nuovo":</b> vi permette di inserire i nuovi utenti al vostro sito: sono presenti dei campi nei quali inserire i dati anagrafici dell'utente, con la possibilit&agrave; di specificare se esso sar&agrave; abilitato al ricevimento delle Newsletter, assegnargli un Ruolo, e cio&egrave; Utente Standard o Amministratore.<br /><br />
						<b>"Visualizza Utenti":</b> questo comando permette di visualizzare, modificare ed eliminare gli utenti del sito. Anche per gli Utenti &egrave; possibile effettuare una ricerca mirata, si possono esportare i dati degli utenti in modo che possano essere letti dal programma Microsoft Excel, sapere quali utenti sono attivi o inattivi, ovvero se possano utilizzare o no s-admin.<br /><br /><br /><br />

						<b>Pulsante "NEWSLETTER"</b><br /><br />
						<img src="img/pulsante_newsletter.jpg"><br /><br />
						Cliccando sulla voce "Newsletter", avete la possibilit&agrave; di creare una e-mail con le offerte, i nuovi arrivi di prodotti o qualsiasi altro contenuto vogliate.<br /><br />
						<b>"Inserimento Newsletter":</b> permette di scrivere le newsletter da inviare agli utenti, con l'Oggetto e la formattazione del testo.<br /><br />
						<b>"Visualizza Newsletter":</b> per visualizzare, modificare, eliminare ed inviare la vostra newsletter a tutti gli utenti del sito. La schermata &egrave; del tutto simile alle altre di gestione di Utenti, Marche e Categorie.<br /><br /><br /><br />
					
						<b>Pulsante "PREVENTIVI"</b><br /><br />
						<img src="img/pulsante_preventivi.jpg"><br /><br />
						Cliccando sulla voce "Preventivi", potete visualizzare o eliminare i preventivi richiesti dai vostri utenti.<br /><br />
						<b>"Visualizza Preventivi":</b> per visualizzare, modificare o eliminare i preventivi. E' possibile come sempre effettuare una ricerca mirata fra tutti i preventivi creati, anche in base ai dati del richiedente, come Nome o Cognome, o addirittura il Mobile richiesto nel preventivo.<br /><br /><br /><br />
						
						<b>Pulsante "OPZIONI"</b><br /><br />
						<img src="img/pulsante_opzioni.jpg"><br /><br />
						Cliccando sulla voce "Opzioni", potete modificare le impostazioni del sito come il messaggio di benvenuto ad un nuovo utente, oppure modificare il testo della Legge sulla Privacy.<p>&nbsp;</p><p>&nbsp;</p>
