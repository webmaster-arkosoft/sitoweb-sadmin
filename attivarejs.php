				2. Scegli "Opzioni". <br />
				3. Scegli "Contenuto" nella navigazione di sinistra. <br />
				4. Seleziona la casella di controllo accanto ad "Abilita JavaScript" e fai clic su OK. 
			</div>	

			<span class="browser">Apple Safari (1.0)</span>
	
			<div class="istruzioni1">
				1. Seleziona "Safari" nel menu superiore. <br />
				2. Scegli "Preferenze". <br />
				3. Scegli "Protezione". <br />
				4. Seleziona la casella di controllo accanto a "Enable JavaScript". 
