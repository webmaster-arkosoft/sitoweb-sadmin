				3. Scegli "Proprietà Web" nella navigazione di sinistra.<br /> 
				4. Seleziona la casella di controllo accanto ad "Abilita JavaScript" e fai clic su OK. 
			</div>
			
			<span class="browser">Mozilla Firefox (1.5)</span>

			<div class="istruzioni1">
