				3. Scegli "Avanzate". <br />
				4. Scegli "Script e Plug-in". <br />
				5. Seleziona la casella di controllo "Abilita JavaScript" e fai clic su OK. 
			</div>
			
			<span class="browser">Mozilla Firefox (1.0)</span>

			<div class="istruzioni1">
